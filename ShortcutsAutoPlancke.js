// Shortcut Command NUMBER 1
TriggerRegister.registerCommand("planckeShortcutsBw").setCommandName("bw");
function planckeShortcutsBw(name) {
  // no name or game supplied
  if (name == undefined) {
new TextComponent("&9/bw <name>").setHoverValue("CaSe-SenSiTivE")
    // return to break out of function
    return;
  }
  // opens website
  if (name != undefined) {
  var link = "http://plancke.io/hypixel/player/stats/" + name + "#Bedwars";
  print(java.awt.Desktop.getDesktop().browse(new java.net.URI(link)));
  event.setCanceled(true);
  ChatLib.chat("&aOpening &b" + link);
}
}
